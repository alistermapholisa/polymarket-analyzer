import {
  int,
  mysqlEnum,
  mysqlTable,
  text,
  timestamp,
  varchar,
  decimal,
  json,
  boolean,
  index,
} from "drizzle-orm/mysql-core";

/**
 * Core user table backing auth flow.
 */
export const users = mysqlTable("users", {
  id: int("id").autoincrement().primaryKey(),
  openId: varchar("openId", { length: 64 }).notNull().unique(),
  name: text("name"),
  email: varchar("email", { length: 320 }),
  loginMethod: varchar("loginMethod", { length: 64 }),
  role: mysqlEnum("role", ["user", "admin"]).default("user").notNull(),
  createdAt: timestamp("createdAt").defaultNow().notNull(),
  updatedAt: timestamp("updatedAt").defaultNow().onUpdateNow().notNull(),
  lastSignedIn: timestamp("lastSignedIn").defaultNow().notNull(),
});

export type User = typeof users.$inferSelect;
export type InsertUser = typeof users.$inferInsert;

/**
 * Market metadata and current state.
 * Stores information about Polymarket markets for quick lookup.
 */
export const markets = mysqlTable(
  "markets",
  {
    id: int("id").autoincrement().primaryKey(),
    polymarketId: varchar("polymarketId", { length: 256 }).notNull().unique(),
    eventId: varchar("eventId", { length: 256 }).notNull(),
    eventName: text("eventName").notNull(),
    marketName: text("marketName").notNull(),
    outcomes: json("outcomes").$type<string[]>().notNull(), // ["Yes", "No"]
    conditionId: varchar("conditionId", { length: 256 }).notNull(),
    enableOrderBook: boolean("enableOrderBook").default(true),
    active: boolean("active").default(true),
    createdAt: timestamp("createdAt").defaultNow().notNull(),
    updatedAt: timestamp("updatedAt").defaultNow().onUpdateNow().notNull(),
  },
  (table) => ({
    polymarketIdIdx: index("polymarketId_idx").on(table.polymarketId),
    eventIdIdx: index("eventId_idx").on(table.eventId),
    activeIdx: index("active_idx").on(table.active),
  })
);

export type Market = typeof markets.$inferSelect;
export type InsertMarket = typeof markets.$inferInsert;

