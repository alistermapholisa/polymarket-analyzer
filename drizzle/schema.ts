/**
 * Predictions generated by the prediction engine.
 * Stores all predictions with signals, outcomes, and accuracy metrics.
 */
export const predictions = mysqlTable(
  "predictions",
  {
    id: int("id").autoincrement().primaryKey(),
    marketId: int("marketId").notNull(),
    polymarketId: varchar("polymarketId", { length: 256 }).notNull(),
    predictedOutcome: varchar("predictedOutcome", { length: 64 }).notNull(), // "Yes" or "No"
    predictedProbability: decimal("predictedProbability", {
      precision: 5,
      scale: 4,
    }).notNull(), // 0.0000 to 1.0000
    confidence: decimal("confidence", { precision: 5, scale: 4 }).notNull(),
    signals: json("signals").$type<Record<string, number>>().notNull(), // {momentum: 0.5, sentiment: 0.7, ...}
    signalWeights: json("signalWeights").$type<Record<string, number>>().notNull(), // {momentum: 0.3, sentiment: 0.7}
    actualOutcome: varchar("actualOutcome", { length: 64 }),
    isAccurate: boolean("isAccurate"),
    marketResolved: boolean("marketResolved").default(false),
    createdAt: timestamp("createdAt").defaultNow().notNull(),
    updatedAt: timestamp("updatedAt").defaultNow().onUpdateNow().notNull(),
  },
  (table) => ({
    marketIdIdx: index("marketId_idx").on(table.marketId),
    polymarketIdIdx: index("polymarketId_idx").on(table.polymarketId),
    createdAtIdx: index("createdAt_idx").on(table.createdAt),
  })
);

export type Prediction = typeof predictions.$inferSelect;
export type InsertPrediction = typeof predictions.$inferInsert;

